package com.chengw.teacherInfoAnnualFillingSystem.model;

public class Education {
    private int eduid;
    private String eduname;
    private String edudate;
    private String edustudent;
    private String edutime;
    private String workload;
    private String mass;
    private String eduscore;
    private String usercode;

    public int getEduid() {
        return eduid;
    }

    public void setEduid(int eduid) {
        this.eduid = eduid;
    }

    public String getEduname() {
        return eduname;
    }

    public void setEduname(String eduname) {
        this.eduname = eduname;
    }

    public String getEdudate() {
        return edudate;
    }

    public void setEdudate(String edudate) {
        this.edudate = edudate;
    }

    public String getEdustudent() {
        return edustudent;
    }

    public void setEdustudent(String edustudent) {
        this.edustudent = edustudent;
    }

    public String getEdutime() {
        return edutime;
    }

    public void setEdutime(String edutime) {
        this.edutime = edutime;
    }

    public String getWorkload() {
        return workload;
    }

    public void setWorkload(String workload) {
        this.workload = workload;
    }

    public String getMass() {
        return mass;
    }

    public void setMass(String mass) {
        this.mass = mass;
    }

    public String getEduscore() {
        return eduscore;
    }

    public void setEduscore(String eduscore) {
        this.eduscore = eduscore;
    }

    public String getUsercode() {
        return usercode;
    }

    public void setUsercode(String usercode) {
        this.usercode = usercode;
    }
}
